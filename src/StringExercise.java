public class StringExercise {
    public static void main(String[] args) {

        String noEducation = "We don't need no education\n";
        noEducation = noEducation +  "We don't need no thought control";
        System.out.println(noEducation);


        String checkThisOut = "Check \"this\" out!,";
        checkThisOut = checkThisOut + " \"s inside of \"s!";
        System.out.println(checkThisOut);


        String inWindows = "In windows,";
        inWindows = inWindows + " the main drive is usually C:\\";
        System.out.println(inWindows);

        String doBackslash = "I can do backslashes \\,";
        doBackslash = doBackslash + " double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(doBackslash);












    }
}
